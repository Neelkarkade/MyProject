package app_javaPratice_17;

public class SwitchCase {
	public static void main(String[] args) {
		int key = 1;
		switch (key) {
		case 1:
			System.out.println(1);
			break;
		case 2:
			System.out.println(2);
			break;
		case 3:
			System.out.println(3);
			break;

		default:
			System.out.println("No match Found");
			break;
		}
	}

}
